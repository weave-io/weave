// Suite summary assembly
// ---------------------------------------------------------------------------

/**
 * Assemble a `SuiteSummaryEntry` from a `BundleScoreFile`.
 *
 * @param scoreFile - The sanitized score file for one suite.
 * @param gitSha - Git SHA at assembly time.
 * @param assembledAt - ISO 8601 assembly timestamp.
 * @returns `ok(SuiteSummaryEntry)` or `err(ReportAssemblyError)`.
 */
export function assembleSuiteSummary(
  scoreFile: BundleScoreFile,
  gitSha: string,
  assembledAt: string,
): Result<SuiteSummaryEntry, ReportAssemblyError> {
  const cases: PublicCaseEntry[] = scoreFile.results.map((row) => {
    return assembleCaseEntry(row, scoreFile.suite);
  });

  const totalCases = cases.length;
  const passedCases = cases.filter((c) => c.passed).length;
  const failedCases = totalCases - passedCases;
  const suiteGreen = cases
    .filter((c) => c.required && !c.dryRun)
    .every((c) => c.passed);

  // Build the suite-level bounded explanation from structured aggregate signals only.
  // The text is derived from integer counts, booleans, and the dryRun flag —
  // never from raw model output, rationale strings, or transcript content.
  const suiteExplanationText = buildSuiteExplanation(
    passedCases,
    totalCases,
    suiteGreen,
    scoreFile.dryRun,
  );
  const suiteExplanationValidated = BoundedExplanationSchema.safeParse({
    text: suiteExplanationText,
    source: "structured_signal",
  });
  const suiteExplanation = suiteExplanationValidated.success
    ? suiteExplanationValidated.data
    : undefined;

  const candidate: SuiteSummaryEntry = {
    schemaVersion: SUITE_SUMMARY_SCHEMA_VERSION,
    suite: scoreFile.suite,
    assembledAt,
    gitSha,
    totalCases,
    passedCases,
    failedCases,
    suiteGreen,
    ...(suiteExplanation !== undefined
      ? { explanation: suiteExplanation }
      : {}),
    cases,
  };

  const parsed = SuiteSummaryEntrySchema.safeParse(candidate);
  if (!parsed.success) {
    return err({
      type: "SchemaValidationFailed",
      schema: "SuiteSummaryEntry",
      message: `SuiteSummaryEntry validation failed: ${parsed.error.issues.map((i) => i.message).join("; ")}`,
    });
  }

  return ok(parsed.data);
}

// ---------------------------------------------------------------------------
// Full public report bundle assembly
// ---------------------------------------------------------------------------

/**
 * Assemble a `PublicReportBundle` from an `EvalBundle`.
 *
 * The bundle is assembled from the sanitized score files in `EvalBundle`.
 * All explanation fields are forwarded from `publicExplanation` in each
 * case result row, validated against `BoundedExplanationSchema`.
 *
 * @param bundle - The assembled `EvalBundle` from `ArtifactBundleWriter`.
 * @param runId - Run identifier (used for dashboard entry path).
 * @returns `ok(PublicReportBundle)` or `err(ReportAssemblyError)`.
 */
export function assemblePublicReportBundle(
  bundle: EvalBundle,
  runId: string,
): Result<PublicReportBundle, ReportAssemblyError> {
  void runId;
  if (bundle.scoreFiles.length === 0) {
    return err({
      type: "EmptyBundle",
      message:
        "Cannot assemble a public report from an empty bundle (no score files).",
    });
  }

  const suiteSummaries: SuiteSummaryEntry[] = [];
  for (const scoreFile of bundle.scoreFiles) {
    const summaryResult = assembleSuiteSummary(
      scoreFile,
      bundle.gitSha,
      bundle.assembledAt,
    );
    if (summaryResult.isErr()) {
      return err(summaryResult.error);
    }
    suiteSummaries.push(summaryResult.value);
  }

  const totalCases = suiteSummaries.reduce((s, ss) => s + ss.totalCases, 0);
  const passedCases = suiteSummaries.reduce((s, ss) => s + ss.passedCases, 0);
  const failedCases = totalCases - passedCases;
  const allSuitesGreen = suiteSummaries.every((ss) => ss.suiteGreen);

  const candidate: PublicReportBundle = {
    schemaVersion: REPORT_BUNDLE_SCHEMA_VERSION,
    assembledAt: bundle.assembledAt,
    gitSha: bundle.gitSha,
    dryRun: bundle.dryRun,
    runSummary: {
      totalCases,
      passedCases,
      failedCases,
      allSuitesGreen,
      suites: suiteSummaries.map((ss) => ss.suite),
    },
    suiteSummaries,
  };

  const parsed = PublicReportBundleSchema.safeParse(candidate);
  if (!parsed.success) {
    return err({
      type: "SchemaValidationFailed",
      schema: "PublicReportBundle",
      message: `PublicReportBundle validation failed: ${parsed.error.issues.map((i) => i.message).join("; ")}`,
    });
  }

  return ok(parsed.data);
}

