/**
 * Assemble a `DashboardManifest` from existing entries plus a new entry.
 *
 * Entries are ordered newest-first. The new entry is prepended to the list.
 * The manifest `totalRuns` is recomputed from the resulting entry count.
 *
 * @param existingEntries - Existing dashboard entries (newest-first).
 * @param newEntry - The new entry to prepend.
 * @param updatedAt - ISO 8601 timestamp when this manifest was updated.
 * @returns `ok(DashboardManifest)` or `err(ReportAssemblyError)`.
 */
export function assembleDashboardManifest(
  existingEntries: DashboardEntry[],
  newEntry: DashboardEntry,
  updatedAt: string,
): Result<DashboardManifest, ReportAssemblyError> {
  const runs = [newEntry, ...existingEntries];

  const candidate: DashboardManifest = {
    schemaVersion: DASHBOARD_MANIFEST_SCHEMA_VERSION,
    updatedAt,
    totalRuns: runs.length,
    runs,
  };

  const parsed = DashboardManifestSchema.safeParse(candidate);
  if (!parsed.success) {
    return err({
      type: "SchemaValidationFailed",
      schema: "DashboardManifest",
      message: `DashboardManifest validation failed: ${parsed.error.issues.map((i) => i.message).join("; ")}`,
    });
  }

  return ok(parsed.data);
}

/**
 * Build a `DashboardEntry` from a `PublicReportBundle` and a run ID.
 *
 * @param bundle - The assembled public report bundle.
 * @param runId - Unique run identifier (e.g. `"abc1234-2026-01-15"`).
 * @param bundleReportPath - Relative path to `public-report.json` in the results repo.
 * @returns A `DashboardEntry` (not yet wrapped in a manifest).
 */
export function buildDashboardEntry(
  bundle: PublicReportBundle,
  runId: string,
  bundleReportPath: string,
): DashboardEntry {
  return {
    runId,
    assembledAt: bundle.assembledAt,
    gitSha: bundle.gitSha,
    dryRun: bundle.dryRun,
    allSuitesGreen: bundle.runSummary.allSuitesGreen,
    totalCases: bundle.runSummary.totalCases,
    passedCases: bundle.runSummary.passedCases,
    failedCases: bundle.runSummary.failedCases,
    suites: bundle.runSummary.suites,
    bundleReportPath,
  };
}

