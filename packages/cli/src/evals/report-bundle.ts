/**
 * Assemble a `ModelComparisonManifest` from a `PublicReportBundle`.
 *
 * Groups per-case entries by model, computes pass rates and per-suite pass rates,
 * and assigns an overall score bucket based on the model's total pass rate.
 *
 * @param bundle - The assembled public report bundle.
 * @param runId - Unique run identifier.
 * @returns `ok(ModelComparisonManifest)` or `err(ReportAssemblyError)`.
 */
export function assembleModelComparisonManifest(
  bundle: PublicReportBundle,
  runId: string,
): Result<ModelComparisonManifest, ReportAssemblyError> {
  // Aggregate per-model, per-suite counts from suite summaries
  const byModel = new Map<
    string,
    {
      total: number;
      passed: number;
      bySuite: Map<string, { total: number; passed: number }>;
    }
  >();

  for (const suiteSummary of bundle.suiteSummaries) {
    for (const caseEntry of suiteSummary.cases) {
      const existing = byModel.get(caseEntry.modelId) ?? {
        total: 0,
        passed: 0,
        bySuite: new Map(),
      };

      existing.total += 1;
      if (caseEntry.passed) existing.passed += 1;

      const suiteStats = existing.bySuite.get(suiteSummary.suite) ?? {
        total: 0,
        passed: 0,
      };
      suiteStats.total += 1;
      if (caseEntry.passed) suiteStats.passed += 1;
      existing.bySuite.set(suiteSummary.suite, suiteStats);
      byModel.set(caseEntry.modelId, existing);
    }
  }

  const models: ModelComparisonEntry[] = [];
  for (const [modelId, stats] of byModel) {
    const passRate = stats.total === 0 ? null : stats.passed / stats.total;
    const perSuitePassRates: Record<string, number | null> = {};
    for (const [suite, suiteStats] of stats.bySuite) {
      perSuitePassRates[suite] =
        suiteStats.total === 0 ? null : suiteStats.passed / suiteStats.total;
    }
    const overallBucket = computeScoreBucket(passRate ?? 0, bundle.dryRun);

    // Build the model-level bounded explanation from structured aggregate signals only.
    // The text is derived from the score bucket enum, integer counts, and the dryRun
    // flag — never from raw model output, rationale strings, or transcript content.
    const modelExplanationText = buildModelExplanation(
      overallBucket,
      stats.passed,
      stats.total,
      bundle.dryRun,
    );
    const modelExplanationValidated = BoundedExplanationSchema.safeParse({
      text: modelExplanationText,
      source: "score_bucket_label",
    });
    const modelExplanation = modelExplanationValidated.success
      ? modelExplanationValidated.data
      : undefined;

    models.push({
      modelId,
      displayName: modelId, // Use modelId as displayName when no model matrix available
      totalCases: stats.total,
      passedCases: stats.passed,
      failedCases: stats.total - stats.passed,
      passRate,
      perSuitePassRates,
      overallBucket,
      ...(modelExplanation !== undefined
        ? { explanation: modelExplanation }
        : {}),
    });
  }

  // Sort by modelId for deterministic output
  models.sort((a, b) => a.modelId.localeCompare(b.modelId));

  const candidate: ModelComparisonManifest = {
    schemaVersion: MODEL_COMPARISON_SCHEMA_VERSION,
    runId,
    assembledAt: bundle.assembledAt,
    gitSha: bundle.gitSha,
    dryRun: bundle.dryRun,
    models,
  };

  const parsed = ModelComparisonManifestSchema.safeParse(candidate);
  if (!parsed.success) {
    return err({
      type: "SchemaValidationFailed",
      schema: "ModelComparisonManifest",
      message: `ModelComparisonManifest validation failed: ${parsed.error.issues.map((i) => i.message).join("; ")}`,
    });
  }

  return ok(parsed.data);
}

