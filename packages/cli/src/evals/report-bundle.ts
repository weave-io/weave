/**
 * Append a new history data point to an existing suite history manifest.
 *
 * @param existing - The current manifest (or null to start fresh).
 * @param suite - The suite name.
 * @param point - The new data point to append.
 * @param updatedAt - ISO 8601 update timestamp.
 * @returns `ok(SuiteHistoryManifest)` or `err(ReportAssemblyError)`.
 */
export function appendSuiteHistoryPoint(
  existing: SuiteHistoryManifest | null,
  suite: string,
  point: SuiteHistoryManifest["history"][number],
  updatedAt: string,
): Result<SuiteHistoryManifest, ReportAssemblyError> {
  const history = existing !== null ? [...existing.history, point] : [point];

  const candidate: SuiteHistoryManifest = {
    schemaVersion: SUITE_HISTORY_SCHEMA_VERSION,
    suite,
    updatedAt,
    history,
  };

  const parsed = SuiteHistoryManifestSchema.safeParse(candidate);
  if (!parsed.success) {
    return err({
      type: "SchemaValidationFailed",
      schema: "SuiteHistoryManifest",
      message: `SuiteHistoryManifest validation failed: ${parsed.error.issues.map((i) => i.message).join("; ")}`,
    });
  }

  return ok(parsed.data);
}
