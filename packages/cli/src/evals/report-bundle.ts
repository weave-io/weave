/**
 * Public report bundle assembly for the Weave agent evals dashboard.
 *
 * Assembles `PublicReportBundle` and `SuiteSummaryEntry` values from
 * sanitized `BundleScoreFile` data. This module owns the final projection
 * step from internal bundle data to the strict public report schemas defined
 * in `report-schema.ts`.
 *
 * # Safety contract
 *
 * All public report fields are derived from allowlisted structured inputs only:
 *   - Pass/fail booleans from `SanitizedCaseResultSummary`
 *   - Score buckets computed from `weightedTotal` via `computeScoreBucket()`
 *   - Bounded explanation text from `publicExplanation` (pre-validated by runner)
 *   - Aggregate counts computed from case-result arrays
 *
 * This module MUST NOT:
 *   - Accept or forward raw model output, transcript content, rationale strings,
 *     chain-of-thought text, prompt text, or LLM-generated freeform summaries.
 *   - Call the scorer or model client.
 *   - Perform file I/O (pure assembly functions only).
 *
 * # Explanation field safety
 *
 * `publicExplanation` fields in `CaseResultSummary` are generated by
 * `buildPublicExplanation()` in `langchain-agent-evals.ts` from pre-enumerated
 * structured inputs before sanitization. This module forwards them into
 * `PublicCaseEntry.explanation` only after validating against
 * `BoundedExplanationSchema` from `report-schema.ts`.
 *
 * Explanations that fail `BoundedExplanationSchema` validation are dropped
 * (explanation field is omitted) — the case entry is still published without
 * an explanation rather than failing the entire report assembly.
 */

import { err, ok, type Result } from "neverthrow";
import {
  buildModelExplanation,
  buildSuiteExplanation,
} from "./langchain-agent-evals.js";
import {
  BoundedExplanationSchema,
  computeScoreBucket,
  DASHBOARD_MANIFEST_SCHEMA_VERSION,
  type DashboardEntry,
  type DashboardManifest,
  DashboardManifestSchema,
  MODEL_COMPARISON_SCHEMA_VERSION,
  type ModelComparisonEntry,
  type ModelComparisonManifest,
  ModelComparisonManifestSchema,
  type PublicCaseEntry,
  type PublicReportBundle,
  PublicReportBundleSchema,
  REPORT_BUNDLE_SCHEMA_VERSION,
  SUITE_HISTORY_SCHEMA_VERSION,
  SUITE_SUMMARY_SCHEMA_VERSION,
  type SuiteHistoryManifest,
  SuiteHistoryManifestSchema,
  type SuiteSummaryEntry,
  SuiteSummaryEntrySchema,
} from "./report-schema.js";
import type { BundleScoreFile, EvalBundle } from "./types.js";

// ---------------------------------------------------------------------------
// Report assembly errors
// ---------------------------------------------------------------------------

/**
 * Typed errors produced during public report assembly.
 */
export type ReportAssemblyError =
  | {
      type: "SchemaValidationFailed";
      /** Which schema was being validated. */
      schema: string;
      /** Human-readable summary. */
      message: string;
    }
  | {
      type: "EmptyBundle";
      message: string;
    };

// ---------------------------------------------------------------------------
// Per-case public entry assembly
// ---------------------------------------------------------------------------

/**
 * Assemble a `PublicCaseEntry` from a sanitized case result row.
 *
 * The explanation field is forwarded only when it passes `BoundedExplanationSchema`
 * validation. Entries failing validation have no `explanation` field (graceful
 * degradation — the score bucket and passed/failed boolean still convey result).
 *
 * @param row - A sanitized case result row from a `BundleScoreFile`.
 * @param suite - The suite name this row belongs to.
 * @returns A valid `PublicCaseEntry` or an assembly error.
 */
export function assembleCaseEntry(
  row: BundleScoreFile["results"][number],
  suite: string,
): PublicCaseEntry {
  const scoreBucket = computeScoreBucket(row.weightedTotal, row.dryRun);

  // Attempt to thread the publicExplanation through BoundedExplanationSchema.
  // Explanations that fail validation are silently dropped — the entry is still
  // valid without an explanation.
  let explanation: PublicCaseEntry["explanation"];
  if (row.publicExplanation !== undefined) {
    const validationResult = BoundedExplanationSchema.safeParse({
      text: row.publicExplanation.text,
      source: row.publicExplanation.source,
    });
    if (validationResult.success) {
      explanation = validationResult.data;
    }
    // When validation fails, explanation is left undefined (graceful drop)
  }

  const entry: PublicCaseEntry = {
    caseId: row.caseId,
    modelId: row.modelId,
    suite,
    scoreBucket,
    passed: row.passed,
    required: row.required,
    dryRun: row.dryRun,
    scoredAt: row.scoredAt,
    ...(explanation !== undefined ? { explanation } : {}),
  };

  return entry;
}

